
import './App.css'
import Sidebar from "./Sidebar.jsx";
import ChatWindow from "./ChatWindow.jsx";
import Chat from "./Chat.jsx";
import { MyContext } from './MyContext.jsx';
import { useState } from 'react';
import {v1 as uuidv1} from 'uuid';

function App() {
  const [prompt,setPrompt] = useState(""); //message from the user
  const [reply,setReply] = useState(null); // message from the backend
  const [currThreadId,setCurrThreadId] = useState(uuidv1()); // it creates unique id
  const [prevChats,setPrevChats]= useState([]);
  const [newChat,setNewChat] =useState(true);
  const [allThreads, setAllThreads] =useState([]);

  const provideValues={
    prompt, setPrompt,
    reply, setReply,
    currThreadId,setCurrThreadId,
    prevChats,setPrevChats,
    newChat,setNewChat,
    allThreads,setAllThreads
  };
  return (
    <div className='main'>
      <MyContext.Provider  value={provideValues}>
      <Sidebar></Sidebar>
      <ChatWindow></ChatWindow>
      </MyContext.Provider>
    </div>
  )
}

export default App
